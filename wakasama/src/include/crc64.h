std::pair<DWORD, DWORD> crc64(const std::vector<BYTE> &buffer) {
  DWORD low = -0x1, high = -0x1, idx = 0;
  for (BYTE c : buffer) {
    idx = ((high >> 0x18 ^ c) & 0xFF) << 3;
    shld(high, low, 0x8);
    high ^= readDwordFromBuffer(CRC_TABLE, idx + 4);
    low <<= 8;
    low ^= readDwordFromBuffer(CRC_TABLE, idx);
  }
  return {~low, ~high};
}